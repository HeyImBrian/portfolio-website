class projectContent {
    constructor(imgs, desc) {
        this.imgs = imgs;
        this.desc = desc;
    }

    getImgs() {
        return this.imgs;
    }

    setImgs(inputImgs) {
        this.imgs = inputImgs;
    }

    getDesc() {
        return this.desc;
    }

    setDesc(inputDesc) {
        this.desc = inputDesc;
    }
}